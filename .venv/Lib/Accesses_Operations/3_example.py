this_l = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]

print(this_l[2:4])  # Output: ['cherry', 'orange']

print(this_l[:4])  # Output: ['apple', 'banana', 'cherry', 'orange']

print(this_l[2:])  # Output: ['cherry', 'orange', 'kiwi', 'melon', 'mango']

print(this_l[1:5:2])  # Output: ['banana', 'orange']

print(this_l[2:6:3])  # Output: ['cherry', 'melon']

print(this_l[-2:-5:-2])  # Output: ['melon', 'orange']
